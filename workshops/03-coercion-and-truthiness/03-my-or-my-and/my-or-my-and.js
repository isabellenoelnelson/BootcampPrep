function myOr(arg1, arg2, arg3) {
    if (arg1) {
        return arg1;
    }
    else if (arg2) {
        return arg2;
    }
    else if (arg3) {
        return arg3;
    }
    else {
        return arg3;
    }
}

function myAnd(arg1, arg2, arg3) {
    if (!arg1) {
        return arg1;
    }
    else if (!arg2) {
        return arg2;
    }
    else if (!arg3) {
        return arg3;
    }
    else {
        return arg3;
    }
}